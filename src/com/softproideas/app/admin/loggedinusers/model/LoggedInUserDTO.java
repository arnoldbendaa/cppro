package com.softproideas.app.admin.loggedinusers.model;

public class LoggedInUserDTO {

    private int loggedInUsersId;
    private String name;
    private String fullName;
    private String clientIp;
    private String clientHost;
    private String creationTime;
    private boolean isAdmin;
    private String lastAccessedTime;
    private String contextId;

    public int getLoggedInUsersId() {
        return loggedInUsersId;
    }

    public void setLoggedInUsersId(int loggedInUsersId) {
        this.loggedInUsersId = loggedInUsersId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getClientIp() {
        return clientIp;
    }

    public void setClientIp(String clientIp) {
        this.clientIp = clientIp;
    }

    public String getClientHost() {
        return clientHost;
    }

    public void setClientHost(String clientHost) {
        this.clientHost = clientHost;
    }

    public String getCreationTime() {
        return creationTime;
    }

    public void setCreationTime(String creationTime) {
        this.creationTime = creationTime;
    }

    public boolean isAdmin() {
        return isAdmin;
    }

    public void setAdmin(boolean isAdmin) {
        this.isAdmin = isAdmin;
    }

    public String getLastAccessedTime() {
        return lastAccessedTime;
    }

    public void setLastAccessedTime(String lastAccessedTime) {
        this.lastAccessedTime = lastAccessedTime;
    }

    public String getContextId() {
        return contextId;
    }

    public void setContextId(String contextId) {
        this.contextId = contextId;
    }

}

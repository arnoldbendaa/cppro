package com.softproideas.app.core.form.model;

public class FormDTO {

    private int formId;
    private boolean defaultForm;
    private String name;
    private String description;
    private String type;

    public int getFormId() {
        return formId;
    }

    public void setFormId(int id) {
        this.formId = id;
    }

    public boolean isDefaultForm() {
        return defaultForm;
    }

    public void setDefaultForm(boolean defaultForm) {
        this.defaultForm = defaultForm;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public String toString() {
        return "FormDTO [formId=" + formId + ", defaultForm=" + defaultForm + ", name=" + name + ", description=" + description + ", type=" + type + "]";
    }

}

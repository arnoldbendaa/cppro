package com.softproideas.app.core.users.model;

public class UserCoreDTO {

    private int userId;
    private String userName;
    private String userFullName;
    private boolean userDisabled;

    public UserCoreDTO() {
    };

    public UserCoreDTO(int userId, String userName, String userFullName, boolean userDisabled) {
        super();
        this.userId = userId;
        this.userName = userName;
        this.userFullName = userFullName;
        this.userDisabled = userDisabled;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUserFullName() {
        return userFullName;
    }

    public void setUserFullName(String userFullName) {
        this.userFullName = userFullName;
    }

    public boolean isUserDisabled() {
        return userDisabled;
    }

    public void setUserDisabled(boolean userDisabled) {
        this.userDisabled = userDisabled;
    }

    @Override
    public String toString() {
        return "UserCoreDTO [userId=" + userId + ", userName=" + userName + ", userFullName=" + userFullName + ", userDisabled=" + userDisabled + "]";
    }

}
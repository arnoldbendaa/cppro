package com.softproideas.app.dashboard.auction.chartemplates;

import java.io.File;
import java.io.IOException;

import org.apache.poi.EncryptedDocumentException;
import org.apache.poi.openxml4j.exceptions.InvalidFormatException;
import org.apache.poi.ss.usermodel.Workbook;
import org.apache.poi.ss.usermodel.WorkbookFactory;
import org.springframework.stereotype.Component;

@Component("xlsTemplate")
public class XlsTemplate {

    private Workbook xlsTemplate;

    public void Init() throws Exception{
        ClassLoader classLoader = getClass().getClassLoader();
        File file = new File(classLoader.getResource("exceltemplates/sale_analysis.xlsx").getFile());
        this.xlsTemplate = WorkbookFactory.create(file);
    }
    public void Init(Workbook xlsTemplate){
    	this.xlsTemplate = xlsTemplate;
    }
    public Workbook getSaleAnalysisXlsTemplate() {
        return xlsTemplate;
    }
}

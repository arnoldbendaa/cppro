package com.softproideas.app.flatformeditor.form.model;

/**
 * <p> 
 * Represents a user attached to Flat Form which has right to use it.
 * </p>
 *
 * @author Jacek Kurasiewicz
 * @email jk@softproideas.com
 * <p>2014 All rights reserved to IT Services Jacek Kurasiewicz</p>
 */
public class UserDTO {
    private int userId;
    private String userLogin;
    private String userFullName;
    
    /**
     * @param userFullName
     * @param userLogin
     */
    public UserDTO(int userId, String userLogin, String userFullName) {
        super();
        this.userId = userId;
        this.userFullName = userFullName;
        this.userLogin = userLogin;
    }
    
    /**
     * @return the userId
     */
    public int getUserId() {
        return userId;
    }
    
    /**
     * @param userId the userId to set
     */
    public void setUserId(int userId) {
        this.userId = userId;
    }

    /**
     * @return the userFullName
     */
    public String getUserFullName() {
        return userFullName;
    }

    /**
     * @param userFullName the userFullName to set
     */
    public void setUserFullName(String userFullName) {
        this.userFullName = userFullName;
    }

    /**
     * @return the userLogin
     */
    public String getUserLogin() {
        return userLogin;
    }

    /**
     * @param userLogin the userLogin to set
     */
    public void setUserLogin(String userLogin) {
        this.userLogin = userLogin;
    }

    /* (non-Javadoc)
     * @see java.lang.Object#hashCode()
     */
    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((userFullName == null) ? 0 : userFullName.hashCode());
        result = prime * result + ((userLogin == null) ? 0 : userLogin.hashCode());
        return result;
    }

    /* (non-Javadoc)
     * @see java.lang.Object#equals(java.lang.Object)
     */
    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        UserDTO other = (UserDTO) obj;
        if (userFullName == null) {
            if (other.userFullName != null)
                return false;
        } else if (!userFullName.equals(other.userFullName))
            return false;
        if (userLogin == null) {
            if (other.userLogin != null)
                return false;
        } else if (!userLogin.equals(other.userLogin))
            return false;
        return true;
    }

    /* (non-Javadoc)
     * @see java.lang.Object#toString()
     */
    @Override
    public String toString() {
        return "UserDTO [userFullName=" + userFullName + ", userLogin=" + userLogin + "]";
    }
    
}

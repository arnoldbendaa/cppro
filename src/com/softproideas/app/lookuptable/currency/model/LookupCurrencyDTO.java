package com.softproideas.app.lookuptable.currency.model;

import java.util.UUID;

public class LookupCurrencyDTO {
    private UUID currencyUUID;
    private int year;
    private int period;
    private int company;
    private String currency;
    private String fieldValue;

    public UUID getCurrencyUUID() {
        return currencyUUID;
    }

    public void setCurrencyUUID(UUID currencyUUID) {
        this.currencyUUID = currencyUUID;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getPeriod() {
        return period;
    }

    public void setPeriod(int period) {
        this.period = period;
    }

    public int getCompany() {
        return company;
    }

    public void setCompany(int company) {
        this.company = company;
    }

    public String getCurrency() {
        return currency;
    }

    public void setCurrency(String currency) {
        this.currency = currency;
    }
    
    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + company;
        result = prime * result + ((currency == null) ? 0 : currency.hashCode());
        result = prime * result + period;
        result = prime * result + year;
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        LookupCurrencyDTO other = (LookupCurrencyDTO) obj;
        if (company != other.company)
            return false;
        if (currency == null) {
            if (other.currency != null)
                return false;
        } else if (!currency.equals(other.currency))
            return false;
        if (period != other.period)
            return false;
        if (year != other.year)
            return false;
        return true;
    }

    public String getFieldValue() {
        return fieldValue;
    }

    public void setFieldValue(String fieldValue) {
        this.fieldValue = fieldValue;
    }

}
